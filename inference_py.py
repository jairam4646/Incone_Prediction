from catboost import CatBoostClassifier
import pandas as pd

model = CatBoostClassifier()
model.load_model(
    "catboost_income_model.cbm"
)

# Create Sample Input
sample_person = pd.DataFrame([
    {
        "age": 39,
        "workclass": "State-gov",
        "fnlwgt": 77516,
        "education": "Bachelors",
        "education_num": 13,
        "marital_status": "Never-married",
        "occupation": "Adm-clerical",
        "relationship": "Not-in-family",
        "race": "White",
        "sex": "Male",
        "capital_gain": 2174,
        "capital_loss": 0,
        "hours_per_week": 40,
        "native_country": "United-States"
    }
])

# Generate Probability
probability = model.predict_proba(
    sample_person
)[0][1]

# Apply Selected Threshold
threshold = 0.40
prediction = (
    ">50K"
    if probability >= threshold
    else "<=50K"
)

# Result
print(
    "Predicted Income Class:",
    prediction
)
print(
    "Probability of Income >50K:",
    round(probability, 4)
)